let firstNumber = Number(prompt("Inserisci il primo numero"));

let secondNumber = Number(prompt("Inserisci il secondo numero"));

let somma = (firstNumber + secondNumber);

let differenza = (firstNumber - secondNumber);

let moltiplicazione = (firstNumber * secondNumber);

let divisione = (firstNumber / secondNumber);

let potenza = (firstNumber ** secondNumber);

console.log("Con i numeri da te scelti i risultati sono: somma = " + somma, 
    "Differenza = " + differenza, "Moltiplicazione = " + moltiplicazione, 
    "divisione = " + divisione,"potenza = " + potenza);